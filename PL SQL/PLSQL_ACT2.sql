CREATE OR REPLACE FUNCTION ISEVEN1 (P_INPUT IN NUMBER)
   RETURN VARCHAR2
AS
   V_RESULT   VARCHAR2 (10);
BEGIN
   IF (P_INPUT MOD 2) = 0
   THEN
      V_RESULT := 'TRUE';
   ELSE
      V_RESULT := 'FALSE';
   END IF;

   RETURN V_RESULT;
END;