DECLARE
   V_FNAME         VARCHAR2 (20);
   V_LNAME         VARCHAR2 (20);
   V_EMPLOYEE_ID   NUMBER;
BEGIN
   SELECT FIRST_NAME, LAST_NAME
     INTO V_FNAME, V_LNAME
     FROM EMPLOYEES
    WHERE EMPLOYEE_ID = '&V_EMPLOYEE_ID';

   DBMS_OUTPUT.PUT_LINE (
      'FIRST NAME: ' || V_FNAME || ' LAST NAME: ' || V_LNAME);
END;


