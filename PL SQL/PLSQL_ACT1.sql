DECLARE
   V_SALARY   VARCHAR2 (20);
   V_EMP_ID   NUMBER;
BEGIN
   SELECT SALARY
     INTO V_SALARY
     FROM EMPLOYEES
    WHERE EMPLOYEE_ID = '&V_EMP_ID';

   DBMS_OUTPUT.PUT_LINE ('SALARY: ' || V_SALARY);

   CASE
      WHEN V_SALARY < 50000
      THEN
         DBMS_OUTPUT.PUT_LINE ('SALARY LESS THAN 50,000');
      ELSE
         DBMS_OUTPUT.PUT_LINE ('SALARY MORE THAN 50,000');
   END CASE;
END;


