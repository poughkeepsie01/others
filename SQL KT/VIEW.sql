CREATE OR REPLACE VIEW PER_ALL_PEOPE_FND_USER
AS
     SELECT PER.FULL_NAME, FND.USER_NAME, PER.DATE_OF_BIRTH
       FROM PER_ALL_PEOPLE_F PER
            LEFT JOIN FND_USER FND ON PER.PERSON_ID = FND.EMPLOYEE_ID
   ORDER BY PER.START_DATE DESC;

