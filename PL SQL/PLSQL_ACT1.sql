DECLARE
   COUNTED_EMP   VARCHAR2 (10);
BEGIN
   SELECT COUNT (EMPLOYEE_ID)
     INTO COUNTED_EMP
     FROM EMPLOYEES
    WHERE DEPARTMENT_ID = 50;

   DBMS_OUTPUT.PUT_LINE ('COUNTED EMPLOYEES IN DEPT.50: ' || COUNTED_EMP);
END;


