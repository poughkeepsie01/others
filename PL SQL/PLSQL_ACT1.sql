DECLARE
   B   NUMBER:= 1;
BEGIN

   WHILE B <= 25
   LOOP
      DBMS_OUTPUT.PUT_LINE (B);
      B := B + 2;
   END LOOP;
END;



DECLARE
   A   NUMBER;
BEGIN
   FOR A IN 1 .. 25
   LOOP
      IF MOD (A, 2) != 0
      THEN
         DBMS_OUTPUT.PUT_LINE (A);
      END IF;
   END LOOP;
END;

