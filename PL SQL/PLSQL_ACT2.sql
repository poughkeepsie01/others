DECLARE
   CURSOR C_EMP
   IS
      SELECT EMPLOYEE_ID, LAST_NAME
        FROM EMPLOYEES
       WHERE DEPARTMENT_ID = 30;

BEGIN
   FOR EMP IN C_EMP
   LOOP
      DBMS_OUTPUT.PUT_LINE (EMP.EMPLOYEE_ID || ' ' || EMP.LAST_NAME);
   END LOOP;
END;

