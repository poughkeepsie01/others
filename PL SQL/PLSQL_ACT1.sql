DECLARE
   V_LNAME    VARCHAR2 (20);
   V_SALARY   VARCHAR2 (20);
BEGIN
   SELECT LAST_NAME, SALARY
     INTO V_LNAME, V_SALARY
     FROM EMPLOYEES
    WHERE JOB_ID = 'AD_PRES';

   DBMS_OUTPUT.PUT_LINE (
      'LAST NAME: ' || V_LNAME || ' ' || 'SALARY: ' || V_SALARY);
END;


