DECLARE
   V_EMPID   EMPLOYEES.EMPLOYEE_ID%TYPE;
   V_LNAME   EMPLOYEES.LAST_NAME%TYPE;

   CURSOR C_EMP
   IS
      SELECT EMPLOYEE_ID, LAST_NAME
        FROM EMPLOYEES
       WHERE DEPARTMENT_ID = 30;

BEGIN
   OPEN C_EMP;

   LOOP
      FETCH C_EMP
         INTO V_EMPID, V_LNAME;

      EXIT WHEN C_EMP%NOTFOUND;
      DBMS_OUTPUT.PUT_LINE (V_EMPID || ' ' || V_LNAME);
   END LOOP;

   CLOSE C_EMP;
END;


