DECLARE
   A   NUMBER;
   B   NUMBER;
   C   NUMBER;
BEGIN
   FOR A IN 1 .. 5
   LOOP
      FOR B IN 1 .. 5
      LOOP
         C := A * B;
         DBMS_OUTPUT.PUT_LINE (A || 'x' || B || ' :' || C);
      END LOOP;
   END LOOP;
END;

