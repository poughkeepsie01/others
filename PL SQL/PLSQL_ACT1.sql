DECLARE
   UPDATED_SAL   NUMBER;
BEGIN
   UPDATE EMPLOYEES
      SET SALARY = SALARY + 500
    WHERE DEPARTMENT_ID = 120;

   IF SQL%NOTFOUND
   THEN
      DBMS_OUTPUT.PUT_LINE ('NO UPDATE');
   ELSIF SQL%FOUND
   THEN
      UPDATED_SAL := SQL%ROWCOUNT;
      DBMS_OUTPUT.PUT_LINE ('UPDATED: ' || UPDATED_SAL || ' RECORD.');
   END IF;
END;

