   V_LAST_NAME    EMPLOYEES.LAST_NAME%TYPE;
   V_JOB_ID       EMPLOYEES.JOB_ID%TYPE;
BEGIN
   SELECT FIRST_NAME, LAST_NAME, JOB_ID
     INTO V_FIRST_NAME, V_LAST_NAME, V_JOB_ID
     FROM EMPLOYEES
    WHERE EMPLOYEE_ID = 200;

